export function useI18n() {
  const ui = useUIStore()
  const locale = computed(() => ui.preferences.language || 'zh-CN')
  const t = computed(() => {
    const lang = locale.value
    const dict = messages[lang] || messages['zh-CN']
    return (key) => dict[key] || messages['zh-CN'][key] || key
  })
  return { t, locale }
}

